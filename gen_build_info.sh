#!/usr/bin/env bash
# gen_build_info.sh — reads the VERSION file and writes Source/BuildInfo.h
# with the current version string and build timestamp in Eastern Time.
# Run this automatically before each build via the workspace build tasks.

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
VERSION_FILE="$SCRIPT_DIR/VERSION"
OUTPUT_FILE="$SCRIPT_DIR/Source/BuildInfo.h"

if [[ ! -f "$VERSION_FILE" ]]; then
    echo "ERROR: VERSION file not found at $VERSION_FILE" >&2
    exit 1
fi

VERSION=$(cat "$VERSION_FILE" | tr -d '[:space:]')
BUILD_TIMESTAMP=$(TZ=America/New_York date '+%b %d %Y  %I:%M %p %Z')

cat > "$OUTPUT_FILE" << HEADER
// BuildInfo.h -- AUTO-GENERATED by gen_build_info.sh -- DO NOT EDIT MANUALLY
// Re-generated on every build.  Commit VERSION, not this file.
#pragma once

#define LOOP_BREAKER_VERSION         "${VERSION}"
#define LOOP_BREAKER_BUILD_TIMESTAMP "${BUILD_TIMESTAMP}"
HEADER

echo "[gen_build_info] v${VERSION}  built ${BUILD_TIMESTAMP}"
